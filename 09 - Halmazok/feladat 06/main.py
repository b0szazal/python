def listaosszefuzes(lista1:List[int], lista2:List[int])->List[int]:
    eredmeny:List[int]=lista1.copy()
    eredmeny+=lista2.copy()

    return eredmeny

def sorbarakas(lista:List[int])->List[int]:
    temp:int=0
    for i in range(0, len(lista)-1, 1):
        for j in range(i+1, len(lista), 1):
            if(lista[j]<lista[i]):
                temp=lista[j]
                lista[j]=lista[i]
                lista[i]=temp

    return lista

def osszeg(lista:List[int])->int:
    eredmeny:int=0
    for item in lista:
        eredmeny+=item

    return eredmeny

