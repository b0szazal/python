def szambeolvasas(minimalisertek:int, maximalisertek:int)->int:
    eredmeny:int=None
    while (eredmeny==None or eredmeny<minimalisertek or eredmeny> maximalisertek):
        data:str=input(f"Kérem a halmaz elemszámát ({minimalisertek} és {maximalisertek} közt):  ")
